#!/usr/bin/env python3

"""Module defines `concat` function"""


def concat(str1: str, str2: str) -> str:
    """Concatenates two strings

    Args:
        str1: The first string
        str2: The second string

    Return:
        The concatenated string

    Example:
        - f("hello", "world") -> "helloworld"
    """
    return str1 + str2
